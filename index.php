      <div class="row">
        <div class="col s12 m8 offset-m2 white-text">
          <h2 class="center-align">Recette : 
          </h2>
        </div>
        <div class="separator col s2 offset-s5 divider">
        </div>
        <div class="col s12 ">
          <img class="responsive-img" src="assets/img/ustencils.png" alt=""> 
          <ol class="flow-text white-text ">
            <li>Préparer les ustensiles (couteau, cuillère, saladier, verre doseur ou balance, papier sulfurisé et plaque allant au four) et les ingrédients, sortir le beurre du frigo et préchauffer le four à 180°C
            </li> 
            <li>Mettre dans le saladier la farine + le sucre + le bicarbonate et mélanger
            </li> 
            <li>Découper le beurre relativement mou en petits dés
            </li>  
            <li>Incorporer le beurre aux ingrédients du saladier en frottant avec les mains. Le résultat doit ressembler à du sable 
            <li>Découper le chocolat en morceaux plus ou moins généreux et incorporer les pépites au mélange
            </li>
            <li>Il est possible de mettre du beurre fondu mais pas trop chaud pour un resultat plus rapide (mais en rognant sur la qualité finale du cookies, quand même ;)) en inversant l'instruction 4 avec la 5 	
            <li>Faire un puit au milieu du mélange et y mettre un oeuf entier
            </li>
            <li>Commencer par mélanger à l'aide d'une cuillère puis malaxer avec les mains propres jusqu'a l'obtention d'une boule de pate homogène
            </li>
          </ol>
        </div>
        <div class="separator col s2 offset-s5 divider">
        </div>
        <div class="col s12">
          <ul class="flow-text white-text center-align">
            <li>Mettre du papier sulfirisé sur une plaque allant au four
            </li>
            <br>
            <li>Prélever des petites boules de pates de même taille, les disposer sur la plaque et les écraser un peu pour former un cookie.
              <br> 
              <br>Il est possible de réaliser un cookies géant en faisant cela avec la boule de pâte entière et en rajoutant 4 minutes de cuisson.
            </li>
          </ul>
        </div>
        <div class="separator col s2 offset-s5 divider">
        </div>
        <div class="col s12 flow-text white-text center-align">
          Mettre au four à 180°C pendant 10 minutes.
          <br>
          <br> ATTENTION : quand vous sortirez les cookies du four, vous allez avoir l'impression qu'ils ne sont pas cuits mais que neni ! En refroidissant, ils deviendront plus ferme et bien moelleux.
        </div>
